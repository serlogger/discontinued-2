function dark_mode() {
    var r = document.querySelector(':root');
    r.style.setProperty('--dark', '#000');
}

function light_mode_blue() {
    var r = document.querySelector(':root');
    r.style.setProperty('--dark', '#669');
}

function light_mode_rosa() {
    var r = document.querySelector(':root');
    r.style.setProperty('--dark', '#c07');
}